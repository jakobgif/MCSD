/**
  * @brief function that blocks the stm32 for a specific time.
  * @param int time: sets the time
  * @retval None
  */
void _tim_timeout_blocking(int time){
	TIM6->ARR = time; //writes time into the auto reload register of timer 6
	HAL_TIM_Base_Start_IT(&htim6); //starts the timer
	HAL_SuspendTick(); //prepare stm32 for sleep mode
	HAL_PWR_EnterSLEEPMode(PWR_MAINREGULATOR_ON, PWR_SLEEPENTRY_WFI); //sets stm32 into sleep mode
	HAL_TIM_Base_Stop_IT(&htim6); //stops the timer after the timer interrupt woke mcu from sleep
}

/**
  * @brief function that executes function after a specific time
  * @param void (*f)(): Pointer to the function that will be executed after the delay time. (note that the function can not have any parameters)
  * 		int time: sets the time
  * @retval None
  */
void _tim_timeout_nonblocking_with_callback(void(*f)(),int time){
	if(delayState==t6off){ //if timer is turned off when function is called
		TIM6->ARR = time; //write time into the auto reload register of timer 6
		HAL_TIM_Base_Start_IT(&htim6); //and start the timer
		delayState = wait; //and set delaystate to wait
	}else if(delayState==wait){
		//wait for timeout to end
	}else if(delayState==resume){ //when the timer interrupt occurred
		(*f)(); //Execute this function
		HAL_TIM_Base_Stop_IT(&htim6); //stop the timer
		delayState = t6off; //and set delaystate to t6off
	}
}

