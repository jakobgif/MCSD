void drawsineDAC(double x){
	int result = (amplitudeHalf * sin(x) + center); //calculate sine wave

	//make sure that value is not bigger than the allowed value, cap it at the max and min (=0) value
	if(result > dacMaxVal){
		result = dacMaxVal;
	}else if (result < 0){
		result = 0;
	}

	if (_dac_setval(result, &hdac1) != 0) //sets the dac to the result of the sine wave calculation
		Error_Handler();

	char buffer[bufferSize];	//generates buffer for uart
	sprintf(buffer, "%d,", result);	//stores the value of result as text in buffer
	if (HAL_UART_Transmit(&huart2, (uint8_t *)buffer, strlen(buffer), 1000) == HAL_ERROR) //writes the result of sine wave calculation to uart
		Error_Handler();

}


