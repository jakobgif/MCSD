	  angle=angle+increment; //angle gets increased by the increment
	  if(angle == sineStep) //if the angle has the the same value as sineStep
		  angle = 0;	//then set angle to 0

	  if (_adc_getval(&read, &hadc1) != 0) //reads the adc value
		  Error_Handler();


	  char buffer[bufferSize]; //uart same as above in drawsineDAC()
